#include "ft_printf.h"

int	ft_putstr(char *s)
{
	int	i;

	if (!s)
	{
		ft_putstr("(null)");
		return (6);
	}
	i = 0;
	while (s[i])
	{
		write (1, &s[i], sizeof(char));
		i++;
	}
	return (ft_strlen(s));
}
